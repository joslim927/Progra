    private void cargarTabla() {
DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
    modelo.setRowCount(0); 

    for (Usuario u : Datos.listaUsuarios) {
        Object[] fila = new Object[5];
        fila[0] = u.getId();
        fila[1] = u.getUsername();
        fila[2] = u.getPassword();
        // Verifica si en tu clase Usuario es isEstado o isActivo
        fila[3] = u.isEstado() ? "Activo" : "Inactivo"; 
        fila[4] = u.getRol(); // Verifica si es getRol o getRole
        
        modelo.addRow(fila);
    }
}


private boolean existeUsuario(String user) {
     for (Usuario u : util.Datos.listaUsuarios) {
        if (u.getUsername().equalsIgnoreCase(user)) {
            return true; // Si lo encuentra, devuelve verdadero
        }
    }
    return false; // Si termina el ciclo y no lo vio, devuelve falso
}

    private void limpiarCampos() {
        // Borra el texto de cada campo
    txtId.setText("");
    txtUsuario.setText("");
    txtPassword.setText("");
    
    // Opcional: pone el foco en el campo de usuario para volver a escribir rápido
    txtUsuario.requestFocus();
    }

    private void configurarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
    String[] titulos = {"ID", "User", "Password", "State", "Role"};
    modelo.setColumnIdentifiers(titulos);
    cargarTabla();
    }


