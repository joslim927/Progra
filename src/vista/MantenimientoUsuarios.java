    private void btnActivarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActivarActionPerformed
        int fila = tblUsuarios.getSelectedRow();

    // 1. Validar que seleccionaron a alguien en la tabla
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un usuario de la tabla para activarlo.");
        return;
    }

    // 2. Obtener el ID de la primera columna (columna 0)
    int id = (int) tblUsuarios.getValueAt(fila, 0);
    
    // 3. Buscar al usuario en la lista y cambiar su estado a true
    for (Usuario u : util.Datos.listaUsuarios) {
        if (u.getId() == id) {
            u.setEstado(true); // Aquí llamamos al método que arreglamos en el paso 1
            break;
        }
    }

    // 4. Actualizar la interfaz
    JOptionPane.showMessageDialog(this, "Usuario activado correctamente.");
    cargarTabla(); // Refresca la tabla para que diga "Activo"
    limpiarCampos(); // Limpia los cuadros de texto
    }//GEN-LAST:event_btnActivarActionPerformed

